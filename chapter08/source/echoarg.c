/*********************************************************
* 用于生成一个可执行文件，用于解释权文件
* 在控制台打印所有的命令行参数
*********************************************************/
#include <stdio.h>

int main(int argc, char *argv[])
{
    int i;
    for(i = 0; i < argc; ++i)
    {
        printf("argv[%d]: %s\n", i, argv[i]);
    }
    return 0;
}
